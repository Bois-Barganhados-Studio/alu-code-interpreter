    private void escArquivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_escArquivoActionPerformed
        try {
            int val = ArqChooser.showOpenDialog(this);
            if (val == JFileChooser.APPROVE_OPTION) {
                txtArqHex.setEnabled(true);
                txtComp.setEnabled(true);
                nameArq.setEnabled(true);
                compilar.setEnabled(true);
                arqUla = ArqChooser.getSelectedFile();
                txtArq.setText("Arquivo: " + arqUla.getName());
            } else {
                txtArqHex.setEnabled(false);
                txtComp.setEnabled(false);
                nameArq.setEnabled(false);
                compilar.setEnabled(false);
                txtArq.setText("Arquivo:");
                txtStatus.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_escArquivoActionPerformed

    private void compilarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_compilarActionPerformed
        try {
            String status = decoder.compileCode(nameArq.getText(),arqUla);
            txtStatus.setText(status);
            if (status.contains("sucesso")) {
                JOptionPane.showMessageDialog(this, "Arquivo compilado com sucesso \n" + status, "Compila√ß√£o completa", JOptionPane.INFORMATION_MESSAGE);
                txtStatus.setForeground(Color.black);
                if (JOptionPane.showConfirmDialog(this, "Deseja enviar o arquivo compilado para o arduino processar?", "Enviar HEX para ardu√≠no", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE) == 0) {
                    try {
                        Runtime.getRuntime().exec("cmd /c start " + new File("comunicadorSerial.exe").getPath() + " " + (!nameArq.getText().equals("") ? nameArq.getText() + ".hex" : arqUla.getName().replace(".ula", "") + ".hex"));
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(this, "Erro desconhecido na comunica√ß√£o:\n" + e.getMessage(), "Erro desconhecido no sistema de envio", JOptionPane.ERROR_MESSAGE);
                        e.printStackTrace();
                    }
                }
                nameArq.setText("");
            } else {
                JOptionPane.showMessageDialog(this, "Erro: " + status, "Erro", JOptionPane.ERROR_MESSAGE);
                txtStatus.setForeground(Color.red);
                nameArq.setText("");
                nameArq.setEnabled(false);
                compilar.setEnabled(false);
                txtArqHex.setEnabled(false);
                txtComp.setEnabled(false);
                txtArq.setText("Arquivo:");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_compilarActionPerformed

